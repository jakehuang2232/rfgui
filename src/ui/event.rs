/// Payload carried by a drag operation. Mirrors the HTML
/// `DataTransfer.items` shape enough for text / file / custom mime
/// round-trips without being locked into the DOM model.
#[derive(Debug, Clone)]
pub enum DragPayload {
    /// Plain UTF-8 text. Maps to `text/plain` on platform clipboards.
    Text(String),
    /// File paths from the OS. Filled by the platform for inbound
    /// drops; outgoing file drags can set this too if the platform
    /// supports file-drag export.
    Files(Vec<std::path::PathBuf>),
    /// Arbitrary mime-tagged binary blob (e.g. `image/png`,
    /// `application/json`).
    Custom {
        mime: SmolStr,
        bytes: Vec<u8>,
    },
}

/// Operations the source allows for a drag — mirrors HTML5
/// `DataTransfer.effectAllowed`. Target handlers set `drop_effect` on
/// [`DragOverEvent`] to pick which one actually applies.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum DragEffect {
    #[default]
    None,
    Copy,
    Move,
    Link,
    /// Any of Copy / Move / Link.
    All,
}

/// Mutable payload container for [`DragStartEvent`] (source fills it)
/// and readable view for [`DragOverEvent`] / [`DropEvent`] (target
/// consumes it). Uses interior mutability so multiple handlers along
/// the bubble path can add items.
#[derive(Clone)]
pub struct DataTransfer {
    state: Rc<RefCell<DataTransferState>>,
}

#[derive(Default)]
struct DataTransferState {
    items: Vec<DragPayload>,
    effect_allowed: DragEffect,
}

impl fmt::Debug for DataTransfer {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        let state = self.state.borrow();
        f.debug_struct("DataTransfer")
            .field("item_count", &state.items.len())
            .field("effect_allowed", &state.effect_allowed)
            .finish()
    }
}

impl DataTransfer {
    pub fn new() -> Self {
        Self {
            state: Rc::new(RefCell::new(DataTransferState::default())),
        }
    }

    pub fn with_items(items: Vec<DragPayload>) -> Self {
        Self {
            state: Rc::new(RefCell::new(DataTransferState {
                items,
                effect_allowed: DragEffect::default(),
            })),
        }
    }

    /// Append a payload item. Called by `dragstart` handlers to seed
    /// what the source is offering.
    pub fn add(&mut self, item: DragPayload) {
        self.state.borrow_mut().items.push(item);
    }

    /// Convenience: push a text item.
    pub fn set_text(&mut self, text: impl Into<String>) {
        self.add(DragPayload::Text(text.into()));
    }

    /// Snapshot current items.
    pub fn items(&self) -> Vec<DragPayload> {
        self.state.borrow().items.clone()
    }

    /// First text item, if any.
    pub fn text(&self) -> Option<String> {
        self.state.borrow().items.iter().find_map(|it| match it {
            DragPayload::Text(s) => Some(s.clone()),
            _ => None,
        })
    }

    /// First file list, if any.
    pub fn files(&self) -> Option<Vec<std::path::PathBuf>> {
        self.state.borrow().items.iter().find_map(|it| match it {
            DragPayload::Files(paths) => Some(paths.clone()),
            _ => None,
        })
    }

    pub fn effect_allowed(&self) -> DragEffect {
        self.state.borrow().effect_allowed
    }

    pub fn set_effect_allowed(&mut self, effect: DragEffect) {
        self.state.borrow_mut().effect_allowed = effect;
    }
}

impl Default for DataTransfer {
    fn default() -> Self {
        Self::new()
    }
}

