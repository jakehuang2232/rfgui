#[derive(Clone)]
pub struct PointerUpUntilHandler {
    id: u64,
    handler: Rc<RefCell<dyn FnMut(&mut PointerUpEvent) -> bool>>,
}

impl PointerUpUntilHandler {
    pub fn new<F>(handler: F) -> Self
    where
        F: FnMut(&mut PointerUpEvent) -> bool + 'static,
    {
        Self {
            id: next_handler_id(),
            handler: Rc::new(RefCell::new(handler)),
        }
    }

    pub fn id(&self) -> u64 {
        self.id
    }

    pub fn call(&self, event: &mut PointerUpEvent) -> bool {
        (self.handler.borrow_mut())(event)
    }
}

impl PartialEq for PointerUpUntilHandler {
    fn eq(&self, other: &Self) -> bool {
        self.id == other.id
    }
}

impl fmt::Debug for PointerUpUntilHandler {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        f.debug_struct("PointerUpUntilHandler")
            .field("id", &self.id)
            .finish()
    }
}

/// Dispatch lifecycle phase.
///
/// Mirrors the W3C DOM `Event.eventPhase` values. rfgui currently runs no
/// capturing phase (no capture-listener API yet); dispatchers set
/// [`EventPhase::AtTarget`] when invoking the hit node and
/// [`EventPhase::Bubbling`] while walking ancestors. [`EventPhase::None`] is
/// the default for freshly built metas and for synthetic events that never
/// bubble (e.g. direct focus/blur).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum EventPhase {
    /// Event not currently being dispatched along a path, or dispatch
    /// completed. Synthetic single-target events also stay in this phase.
    #[default]
    None,
    /// Root-to-target walk. Not yet emitted by rfgui.
    Capturing,
    /// Listeners on the original target run.
    AtTarget,
    /// Target-to-root walk; ancestor listeners run here.
    Bubbling,
}

/// Where an event originated. Distinguishes real platform input from
/// engine-synthesised or user-scripted events so handlers and a11y tooling
/// can react differently (e.g. screen readers only trust `Platform`).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum EventSource {
    /// Produced by the platform backend (winit, DOM, headless host).
    #[default]
    Platform,
    /// Generated by the engine itself (focus queue, hover enter/leave
    /// transitions, pointer-capture releases, …).
    Synthetic,
    /// Dispatched programmatically by user code.
    Scripted,
    /// Injected by an accessibility tool (screen reader, IME test harness).
    Accessibility,
}

/// Scroll alignment requested by [`EventCommand::ScrollIntoView`].
/// Matches the DOM `ScrollLogicalPosition` options used by
/// `Element.scrollIntoView`.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum ScrollAlignment {
    /// Scroll as little as possible to make the target visible.
    #[default]
    Nearest,
    /// Align the target to the start (top for block, left for inline) of
    /// the scroll container.
    Start,
    /// Align the target to the center of the scroll container.
    Center,
    /// Align the target to the end (bottom / right).
    End,
}

/// Options for [`EventCommand::ScrollIntoView`].
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub struct ScrollIntoViewOptions {
    /// Block-axis (vertical) alignment.
    pub block: ScrollAlignment,
    /// Inline-axis (horizontal) alignment.
    pub inline: ScrollAlignment,
    /// Animate the scroll rather than jumping. Engine honours this when
    /// a scroll transition plugin is available; otherwise the action
    /// completes instantly.
    pub smooth: bool,
}

/// Commands a handler can push onto the dispatch queue to request a
/// viewport- or host-level side effect. Applied by the viewport after the
/// event finishes bubbling.
#[derive(Clone, Debug)]
pub enum EventCommand {
    AddPointerMoveListener(PointerMoveHandlerProp),
    AddPointerUpListener(PointerUpHandlerProp),
    AddPointerUpListenerUntil(PointerUpUntilHandler),
    SetFocus(Option<NodeId>),
    SetCursor(Option<Cursor>),
    SelectTextRangeAll(NodeId),
    SelectTextRange {
        target_id: NodeId,
        start: usize,
        end: usize,
    },
    RemoveListener(ViewportListenerHandle),
    // --- Sprint 5 command additions ---
    /// Ask the runner to schedule another redraw.
    RequestRedraw,
    /// Write text to the host clipboard.
    WriteClipboard(String),
    /// Scroll the given node into view inside its nearest scrollable
    /// ancestor.
    ScrollIntoView {
        target_id: NodeId,
        options: ScrollIntoViewOptions,
    },
    /// Acquire (`Some(node_id)`) or release (`None`) the keyboard capture.
    /// While captured, all key events route to that node regardless of
    /// focus (used for modal overlays, tooltips absorbing Esc, …).
    KeyboardCapture(Option<NodeId>),
    /// Host window lifecycle op.
    Window(crate::platform::WindowCommand),
    /// IME control op.
    Ime(crate::platform::ImeCommand),
    // --- Sprint 6 command additions ---
    /// Start a drag operation originating from the current target.
    /// Runner picks up the request via `PlatformRequests.pending_drag`
    /// and hands it to the OS drag machinery.
    StartDrag {
        source_id: NodeId,
        payload: Vec<DragPayload>,
        effect_allowed: DragEffect,
    },
    /// Ask the runner to read the system clipboard and dispatch the
    /// result as a [`PasteEvent`] to the focused node.
    RequestPaste,
}

struct EventMetaState {
    /// Monotonic id assigned at construction. Useful for logs, replays, and
    /// correlating an event across layers.
    event_id: u64,
    // Target / current_target / related_target are kept flat (id + bounds)
    // instead of as owned `EventTarget`s so the stored state has no
    // lifetime. The viewport reference lives in `viewport_ptr` below; each
    // getter re-assembles a lifetime-bound `EventTarget<'_>` on demand.
    target_id: NodeId,
    target_bounds: Rect,
    target_local_bounds: Rect,
    current_target_id: NodeId,
    current_target_bounds: Rect,
    current_target_local_bounds: Rect,
    /// For pair-style events: the "other" node.
    ///
    /// - `PointerEnter` / `PointerLeave`: previously / next-hovered node
    /// - `Focus`: blur-source node (where focus moved from)
    /// - `Blur`: focus-destination node (where focus moved to)
    ///
    /// `None` when no counterpart exists (e.g. first focus, pointer
    /// entering from outside the window).
    related_target_id: Option<NodeId>,
    related_target_bounds: Rect,
    related_target_local_bounds: Rect,
    /// Raw pointer to the dispatching viewport. Set by the dispatch layer
    /// on entry and cleared on exit as a second line of defence against
    /// `meta.clone()` leaks. Used for per-node state accessors
    /// (hovered / focused).
    ///
    /// SAFETY: dereferenced only in `EventTarget` getters, which rebind
    /// the reference to the borrow lifetime of `&self` on `EventMeta`.
    /// The target cannot outlive that borrow, so the pointer's pointee
    /// stays valid for the full lifetime of the returned reference.
    viewport_ptr: Option<NonNull<crate::view::viewport::Viewport>>,
    /// Ancestor chain from `target` up to the containing root (inclusive of
    /// both ends). Populated by the viewport dispatch layer when it has the
    /// tree available; empty for synthetic events where no path was computed.
    path: Rc<Vec<NodeId>>,
    phase: EventPhase,
    source: EventSource,
    bubbles: bool,
    cancelable: bool,
    propagation_stopped: bool,
    /// Set by `stop_immediate_propagation`. Stronger than
    /// `propagation_stopped`: halts remaining listeners at the *current*
    /// target too, not just ancestors.
    immediate_propagation_stopped: bool,
    default_prevented: bool,
    /// Former `keep_focus_requested`. True when a handler asked the
    /// dispatcher to preserve the existing focus in spite of the default
    /// focus-change behaviour (e.g. pointer down inside a popup that
    /// should not steal focus from the owning input).
    focus_change_suppressed: bool,
    /// Set by a handler to signal the platform layer that the event was
    /// consumed (used to swallow system shortcuts, IME passthrough, …).
    /// Independent from `propagation_stopped`: a handler can let the event
    /// keep bubbling for local observers while still claiming the platform
    /// side.
    handled: bool,
    pointer_capture_target_id: Option<NodeId>,
    viewport_listener_actions: Vec<EventCommand>,
}

impl Default for EventMetaState {
    fn default() -> Self {
        Self {
            event_id: next_event_id(),
            target_id: NodeId::default(),
            target_bounds: Rect::default(),
            target_local_bounds: Rect::default(),
            current_target_id: NodeId::default(),
            current_target_bounds: Rect::default(),
            current_target_local_bounds: Rect::default(),
            related_target_id: None,
            related_target_bounds: Rect::default(),
            related_target_local_bounds: Rect::default(),
            viewport_ptr: None,
            path: Rc::new(Vec::new()),
            phase: EventPhase::default(),
            source: EventSource::default(),
            bubbles: true,
            cancelable: true,
            propagation_stopped: false,
            immediate_propagation_stopped: false,
            default_prevented: false,
            focus_change_suppressed: false,
            handled: false,
            pointer_capture_target_id: None,
            viewport_listener_actions: Vec::new(),
        }
    }
}

fn next_event_id() -> u64 {
    static NEXT_ID: AtomicU64 = AtomicU64::new(1);
    NEXT_ID.fetch_add(1, Ordering::Relaxed)
}

#[derive(Clone)]
pub struct EventMeta {
    state: Rc<RefCell<EventMetaState>>,
}

impl fmt::Debug for EventMeta {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        let state = self.state.borrow();
        f.debug_struct("EventMeta")
            .field("event_id", &state.event_id)
            .field("target_id", &state.target_id)
            .field("current_target_id", &state.current_target_id)
            .field("phase", &state.phase)
            .field("source", &state.source)
            .field("propagation_stopped", &state.propagation_stopped)
            .field("default_prevented", &state.default_prevented)
            .field("handled", &state.handled)
            .finish()
    }
}

impl EventMeta {
    pub fn new(target_id: NodeId) -> Self {
        Self {
            state: Rc::new(RefCell::new(EventMetaState {
                target_id,
                current_target_id: target_id,
                ..EventMetaState::default()
            })),
        }
    }

    /// Construct an `EventMeta` seeded with a full [`EventTarget`]
    /// (id + bounds). Prefer this over [`Self::new`] at dispatch sites
    /// that already know the node's geometry.
    pub fn with_target(target: EventTarget<'_>) -> Self {
        Self {
            state: Rc::new(RefCell::new(EventMetaState {
                target_id: target.id,
                target_bounds: target.bounds,
                target_local_bounds: target.local_bounds,
                current_target_id: target.id,
                current_target_bounds: target.bounds,
                current_target_local_bounds: target.local_bounds,
                ..EventMetaState::default()
            })),
        }
    }

    /// Node the event is dispatched to (the hit-test result or focused
    /// node). Stable for the duration of the dispatch. The returned
    /// `EventTarget<'_>`'s lifetime is bound to `&self`, so callers cannot
    /// leak it beyond the current handler call.
    pub fn target(&self) -> EventTarget<'_> {
        let (id, bounds, local_bounds) = {
            let s = self.state.borrow();
            (s.target_id, s.target_bounds, s.target_local_bounds)
        };
        self.rebind(EventTarget::snapshot(id, bounds, local_bounds))
    }

    /// Node currently processing the event as it bubbles. Changes as the
    /// dispatch walks up ancestors.
    pub fn current_target(&self) -> EventTarget<'_> {
        let (id, bounds, local_bounds) = {
            let s = self.state.borrow();
            (
                s.current_target_id,
                s.current_target_bounds,
                s.current_target_local_bounds,
            )
        };
        self.rebind(EventTarget::snapshot(id, bounds, local_bounds))
    }

    pub fn target_id(&self) -> NodeId {
        self.state.borrow().target_id
    }

    pub fn current_target_id(&self) -> NodeId {
        self.state.borrow().current_target_id
    }

    pub(crate) fn set_target_id(&mut self, target_id: NodeId) {
        let mut state = self.state.borrow_mut();
        state.target_id = target_id;
        state.target_bounds = Rect::default();
        state.target_local_bounds = Rect::default();
        state.current_target_id = target_id;
        state.current_target_bounds = Rect::default();
        state.current_target_local_bounds = Rect::default();
    }

    pub(crate) fn set_current_target_id(&mut self, current_target_id: NodeId) {
        self.state.borrow_mut().current_target_id = current_target_id;
    }

    /// Replace the `target` (the node that was originally hit). Usually
    /// called once at dispatch start.
    #[allow(dead_code)]
    pub(crate) fn set_target(&mut self, target: EventTarget<'_>) {
        let mut state = self.state.borrow_mut();
        state.target_id = target.id;
        state.target_bounds = target.bounds;
        state.target_local_bounds = target.local_bounds;
        state.current_target_id = target.id;
        state.current_target_bounds = target.bounds;
        state.current_target_local_bounds = target.local_bounds;
    }

    /// Update the `current_target` as the event bubbles up. Prefer this
    /// over [`Self::set_current_target_id`] so ancestor bounds propagate
    /// into handlers.
    pub(crate) fn set_current_target(&mut self, current_target: EventTarget<'_>) {
        let mut state = self.state.borrow_mut();
        state.current_target_id = current_target.id;
        state.current_target_bounds = current_target.bounds;
        state.current_target_local_bounds = current_target.local_bounds;
    }

    /// Internal: rebind an `EventTarget` to this meta's stored viewport
    /// pointer, producing one whose lifetime is the borrow of `&self`.
    ///
    /// SAFETY: `viewport_ptr` is set by the dispatch layer on entry to a
    /// dispatch call and cleared on exit. While set, the pointee is live
    /// for the full duration of `&self`'s borrow. Returned reference
    /// therefore cannot outlive the pointee.
    fn rebind<'a>(&'a self, mut target: EventTarget<'a>) -> EventTarget<'a> {
        if let Some(ptr) = self.state.borrow().viewport_ptr {
            // SAFETY: see method doc. Raw ptr is only consumed here.
            target.viewport = Some(unsafe { ptr.as_ref() });
        }
        target
    }

    /// Install the dispatching viewport on this meta so lazy
    /// `EventTarget` accessors can walk the tree / read per-node state.
    /// Arena access goes through `viewport.node_arena()` — single source
    /// of truth under Approach C.
    #[allow(dead_code)]
    pub(crate) fn attach_dispatch_ctx(
        &mut self,
        viewport: &crate::view::viewport::Viewport,
    ) {
        self.state.borrow_mut().viewport_ptr = Some(NonNull::from(viewport));
    }

    /// Second line of defence: the dispatch layer calls this on exit so
    /// even if a handler clones the meta out of scope, `EventTarget`
    /// accessors return safe defaults afterwards instead of dereferencing
    /// a possibly-invalidated pointer.
    #[allow(dead_code)]
    pub(crate) fn detach_dispatch_ctx(&mut self) {
        self.state.borrow_mut().viewport_ptr = None;
    }

    /// Monotonic identifier assigned at construction. Stable for the
    /// lifetime of the event; use for log correlation and trace spans.
    pub fn event_id(&self) -> u64 {
        self.state.borrow().event_id
    }

    /// Current dispatch lifecycle phase. See [`EventPhase`].
    pub fn phase(&self) -> EventPhase {
        self.state.borrow().phase
    }

    pub(crate) fn set_phase(&mut self, phase: EventPhase) {
        self.state.borrow_mut().phase = phase;
    }

    /// Origin of the event (platform input, engine-synthetic, user-scripted,
    /// or accessibility tool).
    pub fn source(&self) -> EventSource {
        self.state.borrow().source
    }

    #[allow(dead_code)]
    pub(crate) fn set_source(&mut self, source: EventSource) {
        self.state.borrow_mut().source = source;
    }

    /// True when the event came from real platform input (shorthand for
    /// `source() == EventSource::Platform`). Mirrors DOM `isTrusted`.
    pub fn is_trusted(&self) -> bool {
        self.state.borrow().source == EventSource::Platform
    }

    /// Whether the event bubbles up the ancestor chain. Most pointer/key
    /// events bubble; `PointerEnter` / `PointerLeave` do not.
    pub fn bubbles(&self) -> bool {
        self.state.borrow().bubbles
    }

    #[allow(dead_code)]
    pub(crate) fn set_bubbles(&mut self, bubbles: bool) {
        self.state.borrow_mut().bubbles = bubbles;
    }

    /// Whether [`prevent_default`] has any effect for this event.
    pub fn cancelable(&self) -> bool {
        self.state.borrow().cancelable
    }

    #[allow(dead_code)]
    pub(crate) fn set_cancelable(&mut self, cancelable: bool) {
        self.state.borrow_mut().cancelable = cancelable;
    }

    /// Tell the dispatcher to skip the platform's default action for this
    /// event (e.g. suppress a textarea inserting a character, block the
    /// browser from scrolling on Space). No-op if the event is not
    /// [`cancelable`](Self::cancelable).
    pub fn prevent_default(&mut self) {
        let mut state = self.state.borrow_mut();
        if state.cancelable {
            state.default_prevented = true;
        }
    }

    pub fn default_prevented(&self) -> bool {
        self.state.borrow().default_prevented
    }

    pub fn propagation_stopped(&self) -> bool {
        self.state.borrow().propagation_stopped
    }

    pub fn stop_propagation(&mut self) {
        self.state.borrow_mut().propagation_stopped = true;
    }

    /// Stronger form of [`stop_propagation`]: also prevents any remaining
    /// listeners on the **current** target from running. Mirrors DOM
    /// `stopImmediatePropagation`.
    pub fn stop_immediate_propagation(&mut self) {
        let mut state = self.state.borrow_mut();
        state.propagation_stopped = true;
        state.immediate_propagation_stopped = true;
    }

    pub fn immediate_propagation_stopped(&self) -> bool {
        self.state.borrow().immediate_propagation_stopped
    }

    /// True when a handler has claimed responsibility for the event.
    /// Distinct from propagation: the event may still bubble for observers
    /// while the platform layer treats it as consumed (swallowing system
    /// shortcuts, IME passthrough, accessibility feedback).
    pub fn handled(&self) -> bool {
        self.state.borrow().handled
    }

    /// Mark the event as consumed by the handler. See [`handled`](Self::handled).
    pub fn mark_handled(&mut self) {
        self.state.borrow_mut().handled = true;
    }

    /// Ask the dispatcher to keep the existing focus, overriding the
    /// default focus-change behaviour (e.g. a pointer-down inside a popup
    /// that should not steal focus from the owning input).
    pub fn suppress_focus_change(&mut self) {
        self.state.borrow_mut().focus_change_suppressed = true;
    }

    pub fn focus_change_suppressed(&self) -> bool {
        self.state.borrow().focus_change_suppressed
    }

    pub fn request_pointer_capture(&mut self) {
        let current_target_id = self.state.borrow().current_target_id;
        self.state.borrow_mut().pointer_capture_target_id = Some(current_target_id);
    }

    pub fn pointer_capture_target_id(&self) -> Option<NodeId> {
        self.state.borrow().pointer_capture_target_id
    }

    /// Ancestor chain walked during dispatch: `target` first, root last
    /// (DOM `composedPath()` ordering). Empty when the dispatch site did
    /// not populate it (e.g. synthetic focus events, unit tests).
    ///
    /// Clones the shared path handle — cheap (`Rc` bump). Handlers that
    /// need to iterate many times should cache the returned `Rc`.
    pub fn composed_path(&self) -> Rc<Vec<NodeId>> {
        Rc::clone(&self.state.borrow().path)
    }

    /// Replace the dispatch path. Called by the viewport dispatcher before
    /// walking ancestors; user code should not need this.
    pub(crate) fn set_path(&mut self, path: Vec<NodeId>) {
        self.state.borrow_mut().path = Rc::new(path);
    }

    /// Counterpart node for pair-style events (see
    /// [`EventMetaState::related_target_id`] for semantics per event).
    pub fn related_target(&self) -> Option<EventTarget<'_>> {
        let (id, bounds, local_bounds) = {
            let s = self.state.borrow();
            (
                s.related_target_id?,
                s.related_target_bounds,
                s.related_target_local_bounds,
            )
        };
        Some(self.rebind(EventTarget::snapshot(id, bounds, local_bounds)))
    }

    /// Set the `related_target`. Called by the dispatcher when it knows the
    /// counterpart (previous hover, focus source, …).
    pub(crate) fn set_related_target(&mut self, related: Option<EventTarget<'_>>) {
        let mut state = self.state.borrow_mut();
        match related {
            Some(t) => {
                state.related_target_id = Some(t.id);
                state.related_target_bounds = t.bounds;
                state.related_target_local_bounds = t.local_bounds;
            }
            None => {
                state.related_target_id = None;
                state.related_target_bounds = Rect::default();
                state.related_target_local_bounds = Rect::default();
            }
        }
    }

    pub fn viewport(&self) -> EventViewport {
        EventViewport {
            state: self.state.clone(),
        }
    }

    pub(crate) fn take_viewport_listener_actions(&mut self) -> Vec<EventCommand> {
        std::mem::take(&mut self.state.borrow_mut().viewport_listener_actions)
    }

    pub(crate) fn text_selection_target(&self, target_id: NodeId) -> TextSelectionTarget {
        TextSelectionTarget {
            state: self.state.clone(),
            target_id,
        }
    }
}

#[derive(Clone)]
pub struct EventViewport {
    state: Rc<RefCell<EventMetaState>>,
}

impl fmt::Debug for EventViewport {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        let pending = self.state.borrow().viewport_listener_actions.len();
        f.debug_struct("EventViewport")
            .field("pending_actions", &pending)
            .finish()
    }
}

impl EventViewport {
    pub fn add_pointer_move_listener<F>(&mut self, handler: F) -> ViewportListenerHandle
    where
        F: FnMut(&mut PointerMoveEvent) + 'static,
    {
        let handler_prop = PointerMoveHandlerProp::new(handler);
        let handle = ViewportListenerHandle(handler_prop.id());
        self.state
            .borrow_mut()
            .viewport_listener_actions
            .push(EventCommand::AddPointerMoveListener(handler_prop));
        handle
    }

    pub fn add_pointer_up_listener<F>(&mut self, handler: F) -> ViewportListenerHandle
    where
        F: FnMut(&mut PointerUpEvent) + 'static,
    {
        let handler_prop = PointerUpHandlerProp::new(handler);
        let handle = ViewportListenerHandle(handler_prop.id());
        self.state
            .borrow_mut()
            .viewport_listener_actions
            .push(EventCommand::AddPointerUpListener(handler_prop));
        handle
    }

    pub fn add_pointer_up_listener_until<F>(&mut self, handler: F) -> ViewportListenerHandle
    where
        F: FnMut(&mut PointerUpEvent) -> bool + 'static,
    {
        let handler_prop = PointerUpUntilHandler::new(handler);
        let handle = ViewportListenerHandle(handler_prop.id());
        self.state.borrow_mut().viewport_listener_actions.push(
            EventCommand::AddPointerUpListenerUntil(handler_prop),
        );
        handle
    }

    pub fn remove_listener(&mut self, handle: ViewportListenerHandle) {
        self.state
            .borrow_mut()
            .viewport_listener_actions
            .push(EventCommand::RemoveListener(handle));
    }

    pub fn set_cursor(&mut self, cursor: Option<Cursor>) {
        self.state
            .borrow_mut()
            .viewport_listener_actions
            .push(EventCommand::SetCursor(cursor));
    }

    pub fn set_focus(&mut self, node_id: Option<NodeId>) {
        self.state
            .borrow_mut()
            .viewport_listener_actions
            .push(EventCommand::SetFocus(node_id));
    }

    /// Request another redraw after this event finishes.
    pub fn request_redraw(&mut self) {
        self.push_action(EventCommand::RequestRedraw);
    }

    /// Queue a clipboard write. Runner delivers it to the OS clipboard
    /// after dispatch completes.
    pub fn write_clipboard(&mut self, text: impl Into<String>) {
        self.push_action(EventCommand::WriteClipboard(text.into()));
    }

    /// Scroll the given node into view inside its nearest scrollable
    /// ancestor, using `options` for alignment / smoothness.
    pub fn scroll_into_view(&mut self, target_id: NodeId, options: ScrollIntoViewOptions) {
        self.push_action(EventCommand::ScrollIntoView {
            target_id,
            options,
        });
    }

    /// Take keyboard capture for `node_id`. All subsequent key events
    /// route to this node regardless of focus, until
    /// [`Self::release_keyboard_capture`] or another `acquire` replaces it.
    pub fn acquire_keyboard_capture(&mut self, node_id: NodeId) {
        self.push_action(EventCommand::KeyboardCapture(Some(node_id)));
    }

    /// Release any active keyboard capture.
    pub fn release_keyboard_capture(&mut self) {
        self.push_action(EventCommand::KeyboardCapture(None));
    }

    /// Queue a host-window lifecycle command (close / minimize / title…).
    pub fn window_command(&mut self, command: crate::platform::WindowCommand) {
        self.push_action(EventCommand::Window(command));
    }

    /// Queue an IME control command (enable / disable / cursor rect).
    pub fn ime_command(&mut self, command: crate::platform::ImeCommand) {
        self.push_action(EventCommand::Ime(command));
    }

    /// Start a drag operation from `source_id` with the given payload.
    /// The runner relays the request to the OS drag machinery.
    pub fn start_drag(
        &mut self,
        source_id: NodeId,
        payload: Vec<DragPayload>,
        effect_allowed: DragEffect,
    ) {
        self.push_action(EventCommand::StartDrag {
            source_id,
            payload,
            effect_allowed,
        });
    }

    /// Request a paste from the OS clipboard. The runner drains this,
    /// reads the clipboard asynchronously if needed, and dispatches a
    /// [`PasteEvent`] back to the focused node.
    pub fn request_paste(&mut self) {
        self.push_action(EventCommand::RequestPaste);
    }

    fn push_action(&mut self, action: EventCommand) {
        self.state
            .borrow_mut()
            .viewport_listener_actions
            .push(action);
    }
}

