/// reference to `__rsx_ev_to_handler`, which constrains the closure arg type
/// via `F: FnMut(&mut O::Event)` and converts straight into `H` via `From<F>`
/// — one generic call instead of two.
#[doc(hidden)]
pub trait __RsxHandlerField {
    type Event;
}

#[doc(hidden)]
#[inline]
pub fn __rsx_ev_to_handler<O, F, H>(_field: &O, f: F) -> H
where
    O: __RsxHandlerField,
    F: FnMut(&mut O::Event) + 'static,
    H: From<F>,
{
    H::from(f)
}

#[doc(hidden)]
#[inline]
pub fn __rsx_ev_no_arg_to_handler<O, F, H>(_field: &O, f: F) -> H
where
    O: __RsxHandlerField,
    F: FnMut() + 'static,
    H: From<NoArgHandler<F>>,
{
    H::from(NoArgHandler(f))
}

#[doc(hidden)]
pub fn __rsx_ev_closure<O, F>(_field: &O, f: F) -> F
where
    O: __RsxHandlerField,
    F: FnMut(&mut O::Event) + 'static,
{
    f
}

#[doc(hidden)]
pub fn __rsx_ev_no_arg<O, F>(_field: &O, f: F) -> NoArgHandler<F>
where
    O: __RsxHandlerField,
    F: FnMut() + 'static,
{
    NoArgHandler(f)
}

fn next_handler_id() -> u64 {
    static NEXT_ID: AtomicU64 = AtomicU64::new(1);
    NEXT_ID.fetch_add(1, Ordering::Relaxed)
}

macro_rules! impl_handler_prop {
    ($ty:ident, $event_ty:ty) => {
        impl $ty {
            pub fn new<F>(handler: F) -> Self
            where
                F: for<'a> FnMut(&'a mut $event_ty) + 'static,
            {
                Self {
                    id: next_handler_id(),
                    handler: Rc::new(RefCell::new(handler)),
                }
            }

            pub fn id(&self) -> u64 {
                self.id
            }

            pub fn call(&self, event: &mut $event_ty) {
                (self.handler.borrow_mut())(event);
            }
        }

        impl PartialEq for $ty {
            fn eq(&self, other: &Self) -> bool {
                self.id == other.id
            }
        }

        impl fmt::Debug for $ty {
            fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
                f.debug_struct(stringify!($ty))
                    .field("id", &self.id)
                    .finish()
            }
        }

        impl<F> From<F> for $ty
        where
            F: for<'a> FnMut(&'a mut $event_ty) + 'static,
        {
            fn from(handler: F) -> Self {
                $ty::new(handler)
            }
        }

        impl<F> From<NoArgHandler<F>> for $ty
        where
            F: FnMut() + 'static,
        {
            fn from(handler: NoArgHandler<F>) -> Self {
                let mut f = handler.0;
                $ty::new(move |_event: &mut $event_ty| f())
            }
        }

        impl __RsxHandlerField for ::core::option::Option<$ty> {
            type Event = $event_ty;
        }
    };
}

macro_rules! impl_into_event_handler_prop {
    ($handler_ty:ty, $event_ty:ty, $into_fn:ident) => {
        impl IntoEventHandlerProp<$handler_ty> for $handler_ty {
            fn into_event_handler_prop(self) -> $handler_ty {
                self
            }
        }

        impl<F> IntoEventHandlerProp<$handler_ty> for F
        where
            F: for<'a> FnMut(&'a mut $event_ty) + 'static,
        {
            fn into_event_handler_prop(self) -> $handler_ty {
                <$handler_ty>::new(self)
            }
        }

        impl<F> IntoEventHandlerProp<$handler_ty> for NoArgHandler<F>
        where
            F: FnMut() + 'static,
        {
            fn into_event_handler_prop(self) -> $handler_ty {
                let mut handler = self.0;
                <$handler_ty>::new(move |_event: &mut $event_ty| {
                    handler();
                })
            }
        }

        pub fn $into_fn<H>(handler: H) -> $handler_ty
        where
            H: IntoEventHandlerProp<$handler_ty>,
        {
            handler.into_event_handler_prop()
        }
    };
}

