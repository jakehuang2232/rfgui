/// Physical placement of a key on the keyboard. Orthogonal to the
/// [`Key`] enum — e.g. `Key::ShiftLeft` has `location = Left`, while
/// numpad digits have `location = Numpad`. Mirrors the W3C
/// `KeyboardEvent.location` values so web-origin code ports cleanly.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum KeyLocation {
    /// Default location — most keys on the main block.
    #[default]
    Standard,
    /// Left-hand variant of a split key (Shift / Ctrl / Alt / Meta).
    Left,
    /// Right-hand variant of a split key.
    Right,
    /// Numeric keypad region (separate digits, operators, Enter).
    Numpad,
}

impl KeyLocation {
    /// Derive the conventional location for a [`Key`] variant. Handlers
    /// that want the W3C mapping without having to match on the full
    /// `Key` enum can use this.
    pub fn from_key(key: Key) -> Self {
        use Key::*;
        match key {
            ShiftLeft | ControlLeft | AltLeft | MetaLeft => Self::Left,
            ShiftRight | ControlRight | AltRight | MetaRight => Self::Right,
            NumberPad0 | NumberPad1 | NumberPad2 | NumberPad3 | NumberPad4 | NumberPad5
            | NumberPad6 | NumberPad7 | NumberPad8 | NumberPad9 | NumberPadAdd
            | NumberPadSubtract | NumberPadMultiply | NumberPadDivide | NumberPadDecimal
            | NumberPadEnter | NumberPadEqual => Self::Numpad,
            _ => Self::Standard,
        }
    }
}

